class Department {
    constructor(data) {
        this.name = data.name;
        this.category = data.category;
        this.admissions = data.admissions || {};
    }

    getPercentile() {
        const jeongsi = this.admissions['정시'];
        if (jeongsi?.백분위) return jeongsi.백분위;
        return null;
    }
}

class University {
    constructor(data) {
        this.name = data.name;
        this.aliases = data.aliases || [];
        this.region = data.region;
        this.type = data.type;
        this.basePercentile = data.basePercentile;
        this.scoreFormula = data.scoreFormula || null;
        this.departments = (data.departments || []).map(d => new Department(d));
    }

    getDepartment(name) {
        return this.departments.find(d =>
            d.name === name || d.name.includes(name) || name.includes(d.name)
        );
    }

    getDepartmentsByCategory(category) {
        return this.departments.filter(d => d.category === category);
    }

    getPercentileForDept(deptName) {
        const dept = this.getDepartment(deptName);
        if (dept) {
            const p = dept.getPercentile();
            if (p) return p;
        }
        return this.basePercentile;
    }

    calcConvertedScore(scores, track) {
        const formula = this.scoreFormula?.[track];
        if (!formula) return null;

        let total = 0;
        if (formula.국어 && scores.국어 != null) total += (scores.국어 / 200) * formula.국어;
        if (formula.수학 && scores.수학 != null) total += (scores.수학 / 200) * formula.수학;
        if (formula.탐구 && scores.탐구1 != null) {
            const t1 = scores.탐구1 || 0;
            const t2 = scores.탐구2 || 0;
            total += ((t1 + t2) / 200) * formula.탐구;
        }
        if (formula.영어 && scores.영어 != null) {
            const g = Math.max(1, Math.min(9, Math.floor(scores.영어)));
            total += formula.영어[g - 1] || 0;
        }
        if (formula.한국사 && scores.한국사 != null) {
            const g = Math.max(1, Math.min(9, Math.floor(scores.한국사)));
            total += formula.한국사[g - 1] || 0;
        }
        if (formula.수학가산 && scores.수학선택 === '미적분/기하') {
            total += formula.수학가산;
        }
        if (formula.과탐가산 && scores.탐구유형 === '과탐') {
            total += formula.과탐가산;
        }
        return Math.round(total * 100) / 100;
    }
}

